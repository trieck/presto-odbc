#include "stdafx.h"
#include "Session.h"

///////////////////////////////////////////////////////////////////////////////
Session::Session()
{
}

///////////////////////////////////////////////////////////////////////////////
Session::Session(const Session &rhs)
{
    *this = rhs;
}

///////////////////////////////////////////////////////////////////////////////
Session::~Session()
{
}

///////////////////////////////////////////////////////////////////////////////
Session& Session::operator =(const Session &rhs)
{
    if (this != &rhs) {
        endpoint = rhs.endpoint;
        user = rhs.user;
        schema = rhs.schema;
        catalog = rhs.catalog;
    }

    return *this;
}
